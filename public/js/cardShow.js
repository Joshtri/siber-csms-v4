
    //FUNCTION CARD HSE //
    function showCardHSE() {
        document.getElementById("card-ikhtisar-HSE").style.display = "block";
        document.getElementById("card-ikhtisar-HSE").classList.add("animate__zoomIn");
        document.getElementById("card-ikhtisar-HSE").classList.remove("animate__zoomOut");
        document.getElementById("card-ikhtisar-HSE").classList.remove("hidden");
      }
  
      function hideCardHSE() {
        document.getElementById("card-ikhtisar-HSE").classList.add("hidden");
        document.getElementById("card-ikhtisar-HSE").classList.remove("animate__zoomIn");
        document.getElementById("card-ikhtisar-HSE").classList.add("animate__zoomOut");
      }
  
      //-- BATAS FUNCTION CARD HSE //
  
  
